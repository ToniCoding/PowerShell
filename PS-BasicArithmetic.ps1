# We can do arithmetic operations with PowerShell.
# Variable that stores the result and the two numbers.
$result
$numb1 = 10
$numb2 = 5

# Add
$result = $numb1 + $numb2
Write-Host $result

# Substract
$result = $numb1 - $numb2
Write-Host $result

# Multiply
$result = $numb1 * $numb2
Write-Host $result

# Division
$result = $numb1 % $numb2
Write-Host $result 