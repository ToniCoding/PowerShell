# There are ways to update variables and make something with numbers using operators.
# Incremental operator.
# Result: 2.
$numb = 1
$numb++
Write-Host $numb

# Decremental operator.
# Result: 1.
$numb--
Write-Host $numb

# Multiply operator.
# Result: 3.
$numb*=3
Write-Host $numb

# Divide operator.
# Result: 0.
$numb%=3
Write-Host $numb